    public void guardar(boolean modo) throws IOException {
        Properties p = new Properties();
        p.load(new FileInputStream("conf.ini"));
        p.setProperty("Usuario", this.jTextField1.getText());
        p.setProperty("Password", this.jTextField2.getText());
        //p.setProperty("Base", this.jTextField3.getText());
        p.setProperty("Host", this.jTextField5.getText());
        p.setProperty("Ruta", this.jTextField7.getText());
        p.setProperty("hibernate.hbm2ddl.auto", "none");
        FileOutputStream out = new FileOutputStream("conf.ini");
        p.save(out, "/* properties updated */");
    }

