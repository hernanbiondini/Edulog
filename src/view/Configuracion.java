    public void conf() {
        try {
            Properties p = new Properties();
            p.load(new FileInputStream("conf.ini"));
            this.setUsuario(p.getProperty("Usuario"));
            this.setPassword(p.getProperty("Password"));
            this.setBase(p.getProperty("Base"));
            this.setHost(p.getProperty("Host"));
            this.setPuerto(p.getProperty("Puerto"));
            this.setDriver(p.getProperty("Driver"));
            this.setRuta(p.getProperty("Ruta"));
            this.setPath(p.getProperty("Pathmysqldump"));
            this.setBackup(p.getProperty("Backup"));
            this.setCache(p.getProperty("hibernate.cache.provider_class"));
            this.setStartegy(p.getProperty("hibernate.hbm2ddl.auto"));

        } catch (Exception e) {
            System.out.println("Error Configuracion: " + e);
        }
    }

